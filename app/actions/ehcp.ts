/** Approve a SENCO-reviewed auto-generated EHCP — makes it visible to teachers. */
export async function approveGeneratedEhcp(ehcpId: string): Promise<void> {
  const user = await requireSenco()
  const schoolId = user.schoolId

  const plan = await prisma.ehcpPlan.findFirst({
    where: { id: ehcpId, schoolId },
    select: { id: true, studentId: true },
  })
  if (!plan) throw new Error('EHCP plan not found')

  await prisma.ehcpPlan.update({
    where: { id: ehcpId },
    data: {
      status:          'active',
      approvedBySenco: true,
      approvedAt:      new Date(),
      approvedBy:      user.id,
    },
  })

  // Notify class teachers + HoY
  const [classTeachers, hoys, student] = await Promise.all([
    prisma.classTeacher.findMany({
      where: { class: { enrolments: { some: { userId: plan.studentId } }, schoolId } },
      select: { userId: true },
    }),
    prisma.user.findMany({
      where: { schoolId, role: 'HEAD_OF_YEAR', isActive: true },
      select: { id: true },
    }),
    prisma.user.findUnique({
      where: { id: plan.studentId },
      select: { firstName: true, lastName: true },
    }),
  ])
  const studentName = student ? `${student.firstName} ${student.lastName}` : 'a student'
  const notifyIds = [...new Set([...classTeachers.map(t => t.userId), ...hoys.map(h => h.id)])]

  if (notifyIds.length > 0) {
    await prisma.sendNotification.createMany({
      data: notifyIds.map(recipientId => ({
        schoolId,
        recipientId,
        type: 'ilp_created',
        title: `EHCP approved: ${studentName}`,
        body: `An Education, Health and Care Plan for ${studentName} has been approved. Please review EHCP provision details in your lesson planning.`,
      })),
    })
  }

  revalidatePath('/senco/ehcp')
}

/**
 * Update a single narrative section of an EHCP.
 * Audited only after the EHCP has been approved.
 */
export async function updateEhcpSection(
  ehcpId: string,
  sectionKey: keyof EhcpSections,
  newContent: string,
): Promise<void> {
  const user = await requireSenco()
  const schoolId = user.schoolId

  const plan = await prisma.ehcpPlan.findFirst({
    where: { id: ehcpId, schoolId },
    select: { sections: true, approvedBySenco: true },
  })
  if (!plan) throw new Error('EHCP plan not found')

  const currentSections = (plan.sections ?? {}) as Record<string, string>
  const previousContent = currentSections[sectionKey] ?? ''

  await prisma.ehcpPlan.update({
    where: { id: ehcpId },
    data: { sections: { ...currentSections, [sectionKey]: newContent } },
  })

  if (plan.approvedBySenco && previousContent !== newContent) {
    await writeEHCPAudit({
      ehcpId,
      userId:        user.id,
      userName:      `${user.firstName} ${user.lastName}`,
      userRole:      user.role,
      fieldChanged:  `Section ${sectionKey} — ${SECTION_LABELS[sectionKey] ?? sectionKey}`,
      previousValue: previousContent,
      newValue:      newContent,
      changeType:    !previousContent && newContent ? 'ADDED' : !newContent ? 'DELETED' : 'EDITED',
    })
  }

  revalidatePath('/senco/ehcp')
}

/**
 * Returns audit entries for a given EHCP plan.
 * SENCO/SLT/SCHOOL_ADMIN see all; TEACHER/HOD/HOY see only their own.
 */
export async function getEhcpAuditLog(ehcpId: string): Promise<EhcpAuditEntryRow[]> {
  const user = await requireSencoOrStaff()

  const fullAccess = ['SENCO', 'SLT', 'SCHOOL_ADMIN'].includes(user.role)

  const entries = await prisma.ehcpAuditEntry.findMany({
    where: {
      ehcpId,
      ...(fullAccess ? {} : { userId: user.id }),
    },
    orderBy: { createdAt: 'desc' },
  })

  return entries.map(e => ({
    id:            e.id,
    ehcpId:        e.ehcpId,
    userId:        e.userId,
    userName:      e.userName,
    userRole:      e.userRole,
    fieldChanged:  e.fieldChanged,
    previousValue: e.previousValue,
    newValue:      e.newValue,
    changeType:    e.changeType,
    createdAt:     e.createdAt,
  }))
}
