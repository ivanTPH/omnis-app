/**
 * Called automatically when a student is escalated SEN_SUPPORT → EHCP.
 * Fetches the student's approved ILP and generates a full EHCP via Claude Sonnet.
 * Exported so it can be called directly by SENCO if needed.
 */
export async function generateEHCPFromILP(
  studentId: string,
  triggeredByUserId: string,
): Promise<{ success: boolean; error?: string }> {
  try {
    const apiKey = process.env.ANTHROPIC_API_KEY
    if (!apiKey) return { success: false, error: 'ANTHROPIC_API_KEY not configured' }

    const [student, existingEhcp] = await Promise.all([
      prisma.user.findUnique({
        where: { id: studentId },
        select: {
          id: true, firstName: true, lastName: true, yearGroup: true, schoolId: true,
          school: { select: { name: true } },
          sendStatus: { select: { needArea: true } },
        },
      }),
      prisma.ehcpPlan.findFirst({
        where: {
          studentId,
          status: { in: ['active', 'under_review'] },
        },
        select: { id: true },
      }),
    ])

    if (!student) return { success: false, error: 'Student not found' }

    // Don't overwrite an existing active/under_review EHCP
    if (existingEhcp) return { success: true } // silently skip — plan already exists

    const ilp = await prisma.individualLearningPlan.findFirst({
      where: { schoolId: student.schoolId, studentId, approvedBySenco: true, status: 'active' },
      include: { targets: { where: { status: 'active' } } },
      orderBy: { createdAt: 'desc' },
    })

    if (!ilp) {
      // No approved ILP — create a skeleton EHCP with placeholder sections
      const skeleton: EhcpSections = {
        A: 'Views and aspirations to be completed with student and parents/carers.',
        B: `${student.sendStatus?.needArea ?? 'General learning support'} — full assessment required.`,
        C: 'Health needs assessment pending.',
        D: 'Social care needs assessment pending.',
        E: 'Educational outcomes to be agreed at EHCP planning meeting.',
        F: 'Special educational provision to be specified following EHCP needs assessment.',
        G: 'Health provision to be confirmed by health professionals.',
        H1: 'Local authority social care provision to be confirmed.',
        H2: 'Other social care provision to be confirmed.',
        I: `${student.school.name} (mainstream secondary with SEND support)`,
        J: '',
        K: 'Appendices: 1. Parental views (pending); 2. Educational Psychology report (pending)',
      }

      await prisma.ehcpPlan.create({
        data: {
          schoolId:       student.schoolId,
          studentId,
          localAuthority: 'Pending — to be completed',
          planDate:       new Date(),
          reviewDate:     new Date(Date.now() + 52 * 7 * 24 * 60 * 60 * 1000),
          status:         'under_review',
          createdBy:      triggeredByUserId,
          autoGenerated:  true,
          approvedBySenco: false,
          sections:       skeleton,
        },
      })

      revalidatePath('/senco/ehcp')
      return { success: true }
    }

    // Build prompt and call Claude Sonnet
    const prompt = buildEhcpPrompt(
      student.firstName, student.lastName,
      student.yearGroup ?? 9,
      student.sendStatus?.needArea ?? ilp.sendCategory,
      ilp,
      student.school.name,
    )

    const client = new Anthropic({ apiKey })
    const msg = await client.messages.create({
      model:      'claude-sonnet-4-6',
      max_tokens: 2000,
      system:     'You are a UK SENCO creating an Education, Health and Care Plan. Return ONLY valid JSON, no markdown.',
      messages:   [{ role: 'user', content: prompt }],
    })

    const raw   = (msg.content[0] as { type: string; text: string }).text.trim()
    const match = raw.match(/\{[\s\S]*\}/)
    if (!match) return { success: false, error: 'No JSON in Claude response' }

    const gen = JSON.parse(match[0])

    const sections: EhcpSections = {
      A: String(gen.A ?? ''), B: String(gen.B ?? ''), C: String(gen.C ?? ''),
      D: String(gen.D ?? ''), E: String(gen.E ?? ''), F: String(gen.F ?? ''),
      G: String(gen.G ?? ''), H1: String(gen.H1 ?? ''), H2: String(gen.H2 ?? ''),
      I: String(gen.I  ?? ''), J: '', K: String(gen.K ?? ''),
    }

    // Section E outcomes → structured EhcpOutcome rows
    const eOutcomes: { outcomeText: string; targetDateWeeks: number; successCriteria: string; provisionRequired: string }[] =
      Array.isArray(gen.E_outcomes) ? gen.E_outcomes.slice(0, 5).map((o: Record<string, unknown>) => ({
        outcomeText:       String(o.outcomeText ?? ''),
        targetDateWeeks:   Number(o.targetDateWeeks) || 52,
        successCriteria:   String(o.successCriteria ?? ''),
        provisionRequired: String(o.provisionRequired ?? ''),
      })) : []

    await prisma.ehcpPlan.create({
      data: {
        schoolId:        student.schoolId,
        studentId,
        localAuthority:  'Pending — to be confirmed with LA',
        planDate:        new Date(),
        reviewDate:      new Date(Date.now() + 52 * 7 * 24 * 60 * 60 * 1000),
        status:          'under_review',
        createdBy:       triggeredByUserId,
        autoGenerated:   true,
        approvedBySenco: false,
        ilpId:           ilp.id,
        sections,
        outcomes: {
          create: eOutcomes.map(o => ({
            section:           'E',
            outcomeText:       o.outcomeText,
            targetDate:        new Date(Date.now() + o.targetDateWeeks * 7 * 24 * 60 * 60 * 1000),
            successCriteria:   o.successCriteria,
            provisionRequired: o.provisionRequired || null,
          })),
        },
      },
    })

    // Notify SENCO (self) so they can review the draft
    const sencosInSchool = await prisma.user.findMany({
      where: { schoolId: student.schoolId, role: 'SENCO', isActive: true },
      select: { id: true },
    })
    if (sencosInSchool.length > 0) {
      await prisma.sendNotification.createMany({
        data: sencosInSchool.map(s => ({
          schoolId: student.schoolId,
          recipientId: s.id,
          type: 'ilp_created',
          title: `AI EHCP draft ready: ${student.firstName} ${student.lastName}`,
          body: `An Education, Health and Care Plan has been auto-generated for ${student.firstName} ${student.lastName} from their approved ILP. Please review, edit sections and approve at /senco/ehcp.`,
        })),
      })
    }

    revalidatePath('/senco/ehcp')

    // Auto-generate APDR from ILP (now that EHCP is in place)
    const { generateAPDRForStudent } = await import('./send-support')
    void generateAPDRForStudent(studentId).catch(err =>
      console.error('[generateEHCPFromILP] generateAPDRForStudent failed:', err)
    )

    return { success: true }
  } catch (err) {
    console.error('[generateEHCPFromILP]', err)
    return { success: false, error: String(err).slice(0, 200) }
  }
}

